public class Person {
    // field
    int id;
    String name;
    
    // getter and setter
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    // constructor

    public Person(int id, String name) {
        this.id = id;
        this.name = name;
    }
}